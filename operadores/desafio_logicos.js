// Criar duas expressões que vão resultar em TRUE (Aritméticos/Relacionais/Lógicos)
const verdade1 = (2 == 2) && (5 > 0)
const verdade2 = (1 != 0) || (50 < 0)

console.log(verdade1)
console.log(verdade2)

// Criar duas expressões que vão resultar em FALSE (Aritméticos/Relacionais/Lógicos)
let falso1 = (10 * 10) < 5 || (45 + 60 - 5) > 100
let falso2 = verdade1 != verdade2

console.log(falso1)
console.log(falso2) 